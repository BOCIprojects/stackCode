package com.bioc.beans;

public class CreditAccountDetails {
	
	private String counterpartyName;
	private String accountNo;
	private String subAcc;
	private String accStatus;
	private String accAccType;
	private String businessUnit;
	private String limitType;
	
	public String getCounterpartyName() {
		return counterpartyName;
	}
	public void setCounterpartyName(String counterpartyName) {
		this.counterpartyName = counterpartyName;
	}
	public String getAccountNo() {
		return accountNo;
	}
	public void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}
	public String getSubAcc() {
		return subAcc;
	}
	public void setSubAcc(String subAcc) {
		this.subAcc = subAcc;
	}
	public String getAccStatus() {
		return accStatus;
	}
	public void setAccStatus(String accStatus) {
		this.accStatus = accStatus;
	}
	public String getAccAccType() {
		return accAccType;
	}
	public void setAccAccType(String accAccType) {
		this.accAccType = accAccType;
	}
	public String getBusinessUnit() {
		return businessUnit;
	}
	public void setBusinessUnit(String businessUnit) {
		this.businessUnit = businessUnit;
	}
	public String getLimitType() {
		return limitType;
	}
	public void setLimitType(String limitType) {
		this.limitType = limitType;
	}
	
}
